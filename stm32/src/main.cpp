  pinMode(VCLK, OUTPUT);
  pinMode(HS, OUTPUT);
  pinMode(VS, OUTPUT);

  pinMode(VID0, OUTPUT);
  pinMode(VID1, OUTPUT);
  pinMode(VID2, OUTPUT);
  pinMode(VID3, OUTPUT);

